"""Отдельный markdown-файл правил записи в журнал регистрации 1С."""

from __future__ import annotations

from datetime import date
from pathlib import Path

from .export_analyzer import ExportAnalysis

MANUAL_PLACEHOLDER = "[ЗАПОЛНИТЬ]"


def event_log_rules_path(main_rules_path: Path) -> Path:
    """Путь к файлу правил журнала рядом с основным файлом правил."""
    return main_rules_path.with_name(f"{main_rules_path.stem}-журнал-регистрации.md")


def _event_log_policy_line(ov: dict[str, str]) -> str:
    val = ov.get("tx_event_log")
    if val == "да":
        return (
            "При ошибках и важных событиях **писать в журнал регистрации** "
            "(уровень и событие — по смыслу ситуации)."
        )
    if val == "нет":
        return (
            "**Не писать** в журнал регистрации без явного указания в задаче "
            "(достаточно сообщения пользователю, если нужно)."
        )
    return (
        "Запись в журнал — **по необходимости**: сбои, отказ интеграций, "
        "действия, которые нужно разбирать без участия пользователя."
    )


def generate_event_log_rules_markdown(
    analysis: ExportAnalysis,
    *,
    output_path: Path | None = None,
    manual_overrides: dict[str, str] | None = None,
    main_rules_filename: str | None = None,
) -> str:
    ov = manual_overrides or {}
    config_name = analysis.display_config_name
    today = date.today().isoformat()
    policy = _event_log_policy_line(ov)
    main_name = main_rules_filename or "файл правил разработки"

    body = f"""# Журнал регистрации — правила для AI

> Отдельный регламент к основному файлу правил (`{main_name}`).
> Прикладывайте **оба** файла в задаче, если затрагиваются ошибки, интеграции или фоновые процессы.

---

## Политика записи

- {policy}

---

## Когда писать

- Необработанные исключения и откаты транзакций из-за ошибки.
- Сбои HTTP/файловых обменов, таймауты, повторные неудачные попытки.
- Действия, которые пользователь не увидит в интерфейсе (фон, регламентное задание).
- **Не писать** штатные ветки без ошибки и не дублировать то, что уже ясно из `Сообщить()` пользователю.

## Как писать (BSL)

- Использовать **`ЗаписьЖурналаРегистрации`** (или принятый в проекте общий модуль-обёртку).
- **Уровень:** `Ошибка` — сбой; `Предупреждение` — деградация; `Информация` — значимое событие без сбоя.
- **Событие** — короткое имя по подсистеме (латиница/кириллица, как принято в конфигурации), одно на смысл.
- В **комментарий** — контекст: объект, этап, ключевые параметры **без паролей, токенов, персональных данных**.
- Не логировать полные тела ответов внешних API и большие таблицы — только суть и идентификаторы.

## Запреты

- Не подменять журналом нормальное сообщение пользователю, если он в интерактиве.
- Не писать «на всякий случай» в циклах и на каждой итерации.
- Не менять состав и уровни событий типовой конфигурации без согласования.

---

## Контекст (снимок при генерации)

- **Конфигурация:** {config_name}
- **Корень выгрузки (пример):** `{analysis.export_path}`

---

## История

| Дата | Что изменено |
|------|----------------|
| {today} | Создан или обновлён файл правил журнала регистрации |
"""

    if output_path:
        output_path = Path(output_path)
        output_path.parent.mkdir(parents=True, exist_ok=True)
        output_path.write_text(body, encoding="utf-8")

    return body
